#!/usr/bin/env python
from __future__ import print_function


def pack_tetrominoes():
    for tetromino in tetrominoes:
        print("  {")
        for rotation in tetromino:
            word = 0
            for row in reversed(rotation):
                for digit in reversed(row):
                    word = (word << 1) | digit
            hex_word = '0x' + hex(word)[2:].zfill(4)
            print("    %s," % hex_word)
        print("  },")


